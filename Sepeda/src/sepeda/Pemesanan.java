                    "sepeda.harga,\n" +
                    "statussepeda.`status`\n" +
                    "FROM\n" +
                    "statussepeda ,\n" +
                    "sepeda\n" +
                    "WHERE\n" +
                    "statussepeda.kodeSepeda = sepeda.kodeSepeda AND\n" +
                    "statussepeda.`status` = 'Ready'";

            stat = (com.mysql.jdbc.Statement) connection.prepareStatement(sql);
            rs = stat.executeQuery(sql);

