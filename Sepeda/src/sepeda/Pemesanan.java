    void GetDataSepedaWithKode()
    {   
        sql = "SELECT\n" +
                    "sepeda.jenis,\n" +
                    "sepeda.merek,\n" +
                    "sepeda.warna,\n" +
                    "sepeda.harga\n" +
                    "FROM\n" +
                    "sepeda\n" +
                    "WHERE\n" +
                    "sepeda.kodeSepeda = '"+jLabel21.getText()+"'";
            try{
                stat = (com.mysql.jdbc.Statement) connection.prepareStatement(sql);
                rs = stat.executeQuery(sql);

                if(rs.next()){
                    String jenis = rs.getString("sepeda.jenis");
                    String merek = rs.getString("sepeda.merek");
                    String warna = rs.getString("sepeda.warna");
                    String harga = rs.getString("sepeda.harga");

                    jLabel23.setText(jenis);
                    jLabel22.setText(merek);
                    jLabel24.setText(warna);
                    jLabel12.setText(harga);

                    rs.close();
                    stat.close();
                }else{
                    JOptionPane.showMessageDialog(null, "Mohon Masukkan Kode SDM yang sudah Terdaftar!");
                }
            }catch(HeadlessException | SQLException e){
                    JOptionPane.showMessageDialog(null, e);
        }
    }
    
