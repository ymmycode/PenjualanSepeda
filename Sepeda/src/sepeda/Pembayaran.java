    void GetDataSepedaWithKode()
    {   
        sql = "SELECT\n" +
                    "sepeda.harga\n" +
                    "FROM\n" +
                    "sepeda\n" +
                    "WHERE\n" +
                    "sepeda.kodeSepeda = '"+jLabel28.getText()+"'";
            try{
                stat = (com.mysql.jdbc.Statement) connection.prepareStatement(sql);
                rs = stat.executeQuery(sql);

                if(rs.next()){
                    String harga = rs.getString("sepeda.harga");
                    jLabel18.setText(harga);
                    jLabel21.setText(harga);
                    
                    rs.close();
                    stat.close();
                }else{
                    JOptionPane.showMessageDialog(null, "Mohon Masukkan Kode SDM yang sudah Terdaftar!");
                }
            }catch(HeadlessException | SQLException e){
                    JOptionPane.showMessageDialog(null, e);
        }
    }
    
